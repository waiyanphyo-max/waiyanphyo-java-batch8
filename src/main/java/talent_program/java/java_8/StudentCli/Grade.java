package main.java.talent_program.java.java_8.StudentCli;

public class Grade {
    private int id;
    private int mark;

    public Grade() {
    }

    public Grade(int id, int mark) {
        this.id = id;
        this.mark = mark;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getMark() {
        return mark;
    }

    public void setMark(int mark) {
        this.mark = mark;
    }
}
